import fs from "node:fs"
import path from "node:path"

const workspaceRoot = process.cwd()
const storiesDirectory = path.join(workspaceRoot, "public", "stories")
const outputPath = path.join(workspaceRoot, "lib", "artwork-assets.generated.ts")

const walkFiles = (directory) =>
  fs.readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const absolutePath = path.join(directory, entry.name)

    if (entry.isDirectory()) {
      return walkFiles(absolutePath)
    }

    if (!entry.isFile()) {
      return []
    }

    return [absolutePath]
  })

const artworkPaths = walkFiles(storiesDirectory)
  .map((absolutePath) => {
    const relativePath = path.relative(path.join(workspaceRoot, "public"), absolutePath)
    return `/${relativePath.split(path.sep).join("/")}`
  })
  .sort((first, second) => first.localeCompare(second))

const output = [
  "// This file is generated by scripts/sync-artwork-assets.mjs.",
  "// It intentionally stores lightweight public URLs only; artwork binaries stay in /public.",
  "export const artworkAssetPaths = [",
  ...artworkPaths.map((artworkPath) => `  ${JSON.stringify(artworkPath)},`),
  "] as const",
  "",
].join("\n")

fs.writeFileSync(outputPath, output)
